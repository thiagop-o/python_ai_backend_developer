#if
if 10 >= 5:
    print("10 é Maior")
    
#if / else
if 20 >= 30:
    print("20 é Maior")
else:
    print("20 é menor")
    
#if / elif / else
if 30 >= 50:
    print("30 é Maior que 50")
elif 30 >= 10:
    print("30 é maior que 10")
else:
    print("30 é menor que 50")
    
# if ternario
status = "Sucesso" if 10 > 1 else "Falha"
print(status)